#!/usr/bin/env node
// Generates ios/App/App/HealthKitTypes.swift from src/lib/healthkit-types.json.
// Single source of truth — same JSON also feeds the TS UI catalog.
// Run after editing the JSON. CI drift test (src/lib/healthkit-drift.test.ts)
// fails if the committed Swift file disagrees with the JSON.
import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '..');

const jsonPath = resolve(repoRoot, 'src/lib/healthkit-types.json');
const swiftPath = resolve(repoRoot, 'ios/App/App/HealthKitTypes.swift');

export function generateSwift(catalog) {
	validateCatalog(catalog);
	const types = catalog.types;
	const lines = [];
	lines.push('// Generated by scripts/gen-healthkit-types.mjs from src/lib/healthkit-types.json.');
	lines.push('// DO NOT EDIT MANUALLY. Edit the JSON and run `npm run gen:healthkit`.');
	lines.push('// CI drift test verifies this file matches the JSON on every PR.');
	lines.push('');
	lines.push('import Foundation');
	lines.push('import HealthKit');
	lines.push('');
	lines.push('enum HealthKitTypes {');
	lines.push('');
	lines.push('    /// All read + write types we request from HealthKit, bundled for requestAuthorization.');
	lines.push('    /// Types gated by iOS version are skipped on older OS via @available.');
	lines.push('    /// Types gated by a UserDefaults feature flag are skipped when the flag is false.');
	lines.push('    static func allRequestedTypes() -> (read: Set<HKObjectType>, write: Set<HKSampleType>) {');
	lines.push('        var read: Set<HKObjectType> = []');
	lines.push('        var write: Set<HKSampleType> = []');
	lines.push('');

	for (const t of types) {
		const indent = (n) => '    '.repeat(n);
		const reads = t.access === 'read' || t.access === 'readWrite';
		const writes = t.access === 'write' || t.access === 'readWrite';

		const guards = [];
		const closes = [];
		let depth = 2;

		if (t.iosMin) {
			const major = String(t.iosMin).split('.')[0];
			guards.push(`${indent(depth)}if #available(iOS ${major}.0, *) {`);
			closes.unshift(`${indent(depth)}}`);
			depth += 1;
		}
		if (t.featureFlag) {
			guards.push(`${indent(depth)}if UserDefaults.standard.bool(forKey: "${t.featureFlag}") {`);
			closes.unshift(`${indent(depth)}}`);
			depth += 1;
		}

		lines.push(`${indent(2)}// ${t.label} (${t.kind}, ${t.access}${t.iosMin ? `, iOS ${t.iosMin}+` : ''}${t.featureFlag ? `, flag: ${t.featureFlag}` : ''})`);
		lines.push(...guards);

		if (t.kind === 'quantity') {
			lines.push(`${indent(depth)}if let _t = HKQuantityType.quantityType(forIdentifier: .${t.identifier}) {`);
			if (reads) lines.push(`${indent(depth + 1)}read.insert(_t)`);
			if (writes) lines.push(`${indent(depth + 1)}write.insert(_t)`);
			lines.push(`${indent(depth)}}`);
		} else if (t.kind === 'category') {
			lines.push(`${indent(depth)}if let _t = HKCategoryType.categoryType(forIdentifier: .${t.identifier}) {`);
			if (reads) lines.push(`${indent(depth + 1)}read.insert(_t)`);
			if (writes) lines.push(`${indent(depth + 1)}write.insert(_t)`);
			lines.push(`${indent(depth)}}`);
		} else if (t.kind === 'workout') {
			if (reads) lines.push(`${indent(depth)}read.insert(HKObjectType.workoutType())`);
			if (writes) lines.push(`${indent(depth)}write.insert(HKObjectType.workoutType())`);
		} else if (t.kind === 'workoutRoute') {
			if (reads) lines.push(`${indent(depth)}read.insert(HKSeriesType.workoutRoute())`);
			if (writes) lines.push(`${indent(depth)}write.insert(HKSeriesType.workoutRoute())`);
		}
		// kind === 'medicationDoseEvent' deliberately emits nothing into the standard
		// auth set. iOS 26 medications use per-object authorization
		// (HKHealthStore.requestPerObjectReadAuthorization on userAnnotatedMedicationType).
		// Putting them in requestAuthorization(toShare:read:) aborts the process via
		// _throwIfAuthorizationDisallowedForSharing. See HealthKitPlugin.setMedicationsEnabled
		// which flips the flag AND triggers the per-object auth sheet in one call.
		else if (t.kind === 'medicationDoseEvent') { /* per-object auth; not in this set */ }

		lines.push(...closes);
		lines.push('');
	}

	lines.push('        return (read, write)');
	lines.push('    }');
	lines.push('');
	lines.push('    /// Sample-type list for deletion sweeps. Includes only writable types.');
	lines.push('    static func allWritableTypesForDelete() -> [HKSampleType] {');
	lines.push('        var types: [HKSampleType] = []');
	for (const t of types) {
		if (t.access !== 'write' && t.access !== 'readWrite') continue;
		if (t.kind === 'quantity') {
			lines.push(`        if let _t = HKQuantityType.quantityType(forIdentifier: .${t.identifier}) { types.append(_t) }`);
		} else if (t.kind === 'category') {
			lines.push(`        if let _t = HKCategoryType.categoryType(forIdentifier: .${t.identifier}) { types.append(_t) }`);
		} else if (t.kind === 'workout') {
			lines.push(`        types.append(HKObjectType.workoutType())`);
		} else if (t.kind === 'workoutRoute') {
			lines.push(`        types.append(HKSeriesType.workoutRoute())`);
		}
	}
	lines.push('        return types');
	lines.push('    }');
	lines.push('');
	lines.push('    /// All tsKeys, in the same order as the JSON catalog. Used by checkPermissionStatus');
	lines.push('    /// to populate the status map and by the TS UI to render rows.');
	lines.push('    static let allTsKeys: [String] = [');
	for (const t of types) {
		lines.push(`        "${t.tsKey}",`);
	}
	lines.push('    ]');
	lines.push('');
	lines.push('    /// tsKeys that map to write types. checkPermissionStatus can return real auth status');
	lines.push('    /// for these (iOS hides read auth status by design).');
	lines.push('    static let writeTsKeys: Set<String> = [');
	for (const t of types) {
		if (t.access !== 'write' && t.access !== 'readWrite') continue;
		lines.push(`        "${t.tsKey}",`);
	}
	lines.push('    ]');
	lines.push('');
	lines.push('    /// Resolve a tsKey to its HKObjectType for authorization-status queries.');
	lines.push('    /// Returns nil if the type is unavailable on this iOS version, gated off by a');
	lines.push('    /// feature flag, or unrecognized. Mirrors the gating logic in allRequestedTypes()');
	lines.push('    /// so no caller can accidentally bypass either check.');
	lines.push('    static func objectType(forTsKey key: String) -> HKObjectType? {');
	lines.push('        switch key {');
	for (const t of types) {
		const cases = [];
		// Indent depth: 3 = inside switch case, +1 per nested gate.
		const depths = { 3: '            ', 4: '                ', 5: '                    ' };
		let depth = 3;
		if (t.iosMin) {
			const major = String(t.iosMin).split('.')[0];
			cases.push(`${depths[depth]}if #available(iOS ${major}.0, *) {`);
			depth += 1;
		}
		if (t.featureFlag) {
			cases.push(`${depths[depth]}if UserDefaults.standard.bool(forKey: "${t.featureFlag}") {`);
			depth += 1;
		}
		if (t.kind === 'quantity') {
			cases.push(`${depths[depth]}return HKQuantityType.quantityType(forIdentifier: .${t.identifier})`);
		} else if (t.kind === 'category') {
			cases.push(`${depths[depth]}return HKCategoryType.categoryType(forIdentifier: .${t.identifier})`);
		} else if (t.kind === 'workout') {
			cases.push(`${depths[depth]}return HKObjectType.workoutType()`);
		} else if (t.kind === 'workoutRoute') {
			cases.push(`${depths[depth]}return HKSeriesType.workoutRoute()`);
		} else if (t.kind === 'medicationDoseEvent') {
			// Per-object auth type — not exposed via the standard objectType resolver.
			// Callers should use HKHealthStore.requestPerObjectReadAuthorization directly.
			cases.push(`${depths[depth]}return nil`);
		}
		if (t.featureFlag) {
			depth -= 1;
			cases.push(`${depths[depth]}}`);
		}
		if (t.iosMin) {
			depth -= 1;
			cases.push(`${depths[depth]}}`);
		}
		if (t.iosMin || t.featureFlag) {
			cases.push(`${depths[3]}return nil`);
		}
		lines.push(`        case "${t.tsKey}":`);
		for (const c of cases) lines.push(c);
	}
	lines.push('        default: return nil');
	lines.push('        }');
	lines.push('    }');
	lines.push('}');
	lines.push('');

	return lines.join('\n');
}

// Hard-fail fast if any string field contains characters that aren't safe to
// interpolate directly into Swift source. Generated file goes through the
// compiler; an unescaped " or backslash from JSON would silently corrupt it.
const SAFE = /^[a-zA-Z0-9._-]+$/;
export function validateCatalog(catalog) {
	const fields = ['tsKey', 'identifier', 'featureFlag'];
	for (const t of catalog.types) {
		for (const f of fields) {
			const v = t[f];
			if (v == null) continue;
			if (!SAFE.test(v)) {
				throw new Error(`healthkit-types.json: field "${f}" of entry "${t.tsKey ?? '?'}" contains unsafe chars for Swift interpolation: ${JSON.stringify(v)}. Allowed: [a-zA-Z0-9._-]`);
			}
		}
	}
}

function main() {
	const catalog = JSON.parse(readFileSync(jsonPath, 'utf8'));
	const swift = generateSwift(catalog);
	const existing = (() => { try { return readFileSync(swiftPath, 'utf8'); } catch { return null; } })();
	if (existing === swift) {
		console.log('[gen-healthkit-types] up to date');
		return;
	}
	writeFileSync(swiftPath, swift);
	console.log(`[gen-healthkit-types] wrote ${swiftPath}`);
}

if (process.argv[1] && resolve(process.argv[1]) === resolve(fileURLToPath(import.meta.url))) {
	main();
}
