#!/usr/bin/env node
/**
 * Read an exercise-muscle-audit CSV and emit migration 027 with UUID-keyed
 * UPDATEs for every row where primary or secondary changed.
 *
 * Run: node scripts/generate-audit-migration.mjs <csv-path>
 *      Writes src/db/migrations/027_exercise_muscle_audit.sql
 *
 * Filters: high + medium confidence only. The validate_exercise_muscles
 * trigger from migration 023 is the safety net — any non-canonical slug
 * proposed by the LLM gets rejected at INSERT/UPDATE time.
 */

import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');

const csvPath = process.argv[2];
if (!csvPath) {
  console.error('usage: generate-audit-migration.mjs <csv-path>');
  process.exit(1);
}

function parseRow(line) {
  const out = [];
  let cur = '';
  let inQuote = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (inQuote) {
      if (c === '"' && line[i + 1] === '"') { cur += '"'; i++; }
      else if (c === '"') inQuote = false;
      else cur += c;
    } else {
      if (c === '"') inQuote = true;
      else if (c === ',') { out.push(cur); cur = ''; }
      else cur += c;
    }
  }
  out.push(cur);
  return out;
}

const lines = readFileSync(csvPath, 'utf8').split('\n');
const rows = lines.slice(1).filter(Boolean).map(parseRow);

console.log(`Read ${rows.length} rows from ${csvPath}`);

const updates = [];
let primarySkipped = 0;
let secondarySkipped = 0;

for (const r of rows) {
  const [uuid, title, currentPrimary, proposedPrimary, primaryChanged,
         currentSecondary, proposedSecondary, secondaryChanged, confidence] = r;

  // Apply both high and medium confidence — validation trigger is a safety net.
  if (confidence !== 'high' && confidence !== 'medium') continue;

  const primaryDelta = primaryChanged === 'CHANGED';
  const secondaryDelta = secondaryChanged === 'CHANGED';
  if (!primaryDelta && !secondaryDelta) continue;

  // Reject empty primary — the trigger requires non-empty primary_muscles
  // for the catalog invariant. Also defensive: never propose a slug that
  // wasn't on the canonical list (script already filters client-side).
  const primaryArr = (proposedPrimary || '').split('|').filter(Boolean);
  const secondaryArr = (proposedSecondary || '').split('|').filter(Boolean);

  if (primaryDelta && primaryArr.length === 0) {
    primarySkipped++;
    continue;
  }

  // Only update fields that actually changed.
  const sets = [];
  if (primaryDelta) sets.push(`primary_muscles = '${JSON.stringify(primaryArr)}'::jsonb`);
  if (secondaryDelta) sets.push(`secondary_muscles = '${JSON.stringify(secondaryArr)}'::jsonb`);

  updates.push({
    uuid,
    title,
    sets: sets.join(', '),
    primaryDelta,
    secondaryDelta,
    confidence,
    currentPrimary,
    proposedPrimary,
    currentSecondary,
    proposedSecondary,
  });
}

console.log(`  ${updates.length} UPDATEs generated`);
console.log(`  ${primarySkipped} skipped (would have set empty primary)`);

const sqlLines = [
  '-- Migration 024: Exercise muscle audit — applied from AI-assisted audit pass.',
  '--',
  '-- Generated by scripts/generate-audit-migration.mjs from',
  `--   ${csvPath.replace(process.env.HOME || '', '~')}`,
  '--',
  '-- Each UPDATE keys on exercise UUID. The validate_exercise_muscles trigger',
  '-- from migration 023 is the safety net — any non-canonical slug fails here.',
  '-- Confidence: high + medium only. Low-confidence rows skipped (none in this',
  '-- run, but the filter is in the generator).',
  '',
  'BEGIN;',
  '',
];

for (const u of updates) {
  const summary = [];
  if (u.primaryDelta) summary.push(`primary: ${u.currentPrimary || '∅'} → ${u.proposedPrimary}`);
  if (u.secondaryDelta) summary.push(`secondary: ${u.currentSecondary || '∅'} → ${u.proposedSecondary}`);
  // Comment may contain pipes/colons but never quotes/semicolons we care about,
  // and the splitter strips line comments before sending.
  sqlLines.push(`-- ${u.title} [${u.confidence}] — ${summary.join('; ')}`);
  sqlLines.push(`UPDATE exercises SET ${u.sets} WHERE uuid = '${u.uuid}';`);
  sqlLines.push('');
}

sqlLines.push('-- Post-flight: verify every exercise still has non-empty primary_muscles');
sqlLines.push('-- (defense in depth — generator filters this above too).');
sqlLines.push('DO $$');
sqlLines.push('DECLARE empty_count INT;');
sqlLines.push('BEGIN');
sqlLines.push('  SELECT COUNT(*) INTO empty_count FROM exercises WHERE primary_muscles = \'[]\'::jsonb;');
sqlLines.push('  IF empty_count > 0 THEN');
sqlLines.push('    RAISE WARNING \'Migration 024: % exercise(s) still have empty primary_muscles after audit. Re-run audit with --slug to retry, or hand-tag.\', empty_count;');
sqlLines.push('  END IF;');
sqlLines.push('END $$;');
sqlLines.push('');
sqlLines.push('COMMIT;');

const outPath = join(ROOT, 'src/db/migrations/027_exercise_muscle_audit.sql');
writeFileSync(outPath, sqlLines.join('\n') + '\n');
console.log(`Wrote ${outPath}`);
